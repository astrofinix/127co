CREATE TRIGGER UpdateLoanPayable
AFTER INSERT ON CL_Transaction
FOR EACH ROW
BEGIN
    DECLARE PaidAmount DECIMAL(8, 2);
    DECLARE PayableAmount DECIMAL(8, 2);
    
    SELECT Loan_AmountPaid, Loan_AmountPayable
    INTO PaidAmount, PayableAmount
    FROM LoanRecords
    WHERE Loan_ID = NEW.Loan_ID;
    
    IF NEW.Transaction_Type = 'Payment' THEN
		UPDATE LoanRecords
		SET Loan_AmountPaid = PaidAmount + NEW.Amount,
			Loan_AmountPayable = PayableAmount - NEW.Amount,
            Loan_Status = CASE
							WHEN Loan_AmountPayable > 0 AND CURRENT_DATE() < Loan_Deadline THEN 'Paying'
                            WHEN Loan_AmountPayable = 0 THEN 'Paid'
                            ELSE 'Overdue'
						END
		WHERE Loan_ID = NEW.Loan_ID;
	END IF;
    
END //

CREATE TRIGGER UpdateSavingsAmount
AFTER INSERT ON CS_Transaction
FOR EACH ROW
BEGIN
    DECLARE Balance DECIMAL(8, 2);

    SELECT Savings_Amount INTO Balance
    FROM SavingsAccounts
    WHERE Savings_ID = NEW.Savings_ID;

    IF NEW.Transaction_Type = 'Deposit' THEN
        UPDATE SavingsAccounts
        SET Savings_Amount = Balance + NEW.Amount
        WHERE Savings_ID = NEW.Savings_ID;
    ELSEIF NEW.Transaction_Type = 'Withdraw' THEN
        UPDATE SavingsAccounts
        SET Savings_Amount = Balance - NEW.Amount
        WHERE Savings_ID = NEW.Savings_ID;
    END IF;
END //

CREATE TRIGGER CheckAmountPayable
BEFORE INSERT ON CL_Transaction
FOR EACH ROW
BEGIN
	DECLARE AmountPayable DECIMAL(8, 2);
    
    SELECT Loan_AmountPayable INTO AmountPayable
    FROM LoanRecords
    WHERE Loan_ID = NEW.Loan_ID;
    
    IF AmountPayable < NEW.Amount AND NEW.Transaction_Type = 'Payment' THEN
		SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Transaction amount exceeds amount payable of the loan account';
	END IF;
END //
    
CREATE TRIGGER CheckWithdrawAmount
BEFORE INSERT ON CS_Transaction
FOR EACH ROW
BEGIN
	DECLARE Amount DECIMAL(8, 2);
    
    SELECT Savings_Amount INTO Amount
    FROM SavingsAccounts
    WHERE Savings_ID = NEW.Savings_ID;
    
    IF Amount < NEW.Amount AND NEW.Transaction_Type = 'Withdraw' THEN
		SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Not enough funds in account!';
	END IF;
END //

CREATE TRIGGER ComputeMonthlyInterest
BEFORE INSERT ON LoanRecords
FOR EACH ROW
BEGIN
    SET NEW.Loan_MonthlyInterest = NEW.Loan_TotalAmount * NEW.Loan_InterestRate, 
    NEW.Loan_AmountPayable = NEW.Loan_TotalAmount;
END //

CREATE TRIGGER InsertInCL_Transactions
AFTER INSERT ON LoanRecords
FOR EACH ROW
BEGIN
	DECLARE ID INT(9);
    
    SET ID = FLOOR(RAND()*(999999999-100000000+1)+100000000);
    
	INSERT INTO CL_Transaction(CLTransaction_ID, Loan_ID, Transaction_Type, Transaction_Date, Transaction_Time, Amount)
    VALUES
    (ID, NEW.Loan_ID, 'Loan', NEW.Loan_Date, NEW.Loan_Time, NEW.Loan_TotalAmount);
END //

CREATE PROCEDURE UpdateMemberStatus()
BEGIN
	UPDATE Membership
    SET Member_Status = CASE 
							WHEN Member_Status = 'Active' AND TIMESTAMPDIFF(DAY, Member_MeetAttended, CURRENT_DATE()) > 365 THEN 'Inactive'
                            ELSE Member_Status
						END;
END //

CREATE PROCEDURE UpdateLoanStatus()
BEGIN
	UPDATE LoanRecords
    SET Loan_Status = CASE
							WHEN Loan_AmountPayable > 0 AND CURRENT_DATE() < Loan_Deadline THEN 'Paying'
                            WHEN Loan_AmountPayable = 0 THEN 'Paid'
                            ELSE 'Overdue'
						END;
END //

CREATE PROCEDURE UpdateLoanInterest()
BEGIN
	UPDATE LoanRecords
    SET Loan_AmountPayable = CASE
								WHEN Loan_Status != 'Paid' AND (TIMESTAMPDIFF(DAY, Loan_Date, CURRENT_DATE()) % 31) + 1 = 0 THEN Loan_AmountPayable + Loan_MonthlyInterest
								ELSE Loan_AmountPayable
							END,
        Loan_CumulativeInterest = CASE
								WHEN Loan_Status != 'Paid' AND (TIMESTAMPDIFF(DAY, Loan_Date, CURRENT_DATE()) % 31) + 1 = 0 THEN Loan_CumulativeInterest + Loan_MonthlyInterest
                                ELSE Loan_CumulativeInterest
							END;
END //

CREATE PROCEDURE UpdateSavingsInterest()
BEGIN
	UPDATE SavingsAccounts
    SET Savings_PreviousInterest = Savings_Amount * Savings_InterestRate/90,
		Savings_Amount = Savings_Amount + (Savings_Amount * Savings_InterestRate/90);
END//

CREATE PROCEDURE CallAll()
BEGIN
	CALL UpdateMemberStatus();
    CALL UpdateLoanStatus();
    CALL UpdateLoanInterest();
    CALL UpdateSavingsInterest();
END //

