
CREATE TABLE IF NOT EXISTS Request(Request_ID VARCHAR(9) NOT NULL UNIQUE,
						Employee_ID INTEGER(8) NOT NULL,
                        Request_Type ENUM('Loan Application', 'Savings Account', 'Membership') NOT NULL,
                        Request_Status ENUM('Accepted', 'Rejected', 'Pending') NOT NULL DEFAULT 'Pending',
                        PRIMARY KEY (Request_ID),
                        FOREIGN KEY (Employee_ID) REFERENCES Employee(Employee_ID) ON UPDATE CASCADE);
                        
CREATE TABLE IF NOT EXISTS Positions(Position VARCHAR(40) NOT NULL UNIQUE,
						PRIMARY KEY (Position));
INSERT INTO Positions
VALUES ('CEO'), ('Vice CEO'), ('Secretary'), ('Treasurer'), ('Auditor'), ('Collections Director'), ('Loans Director'),
('Savings Director'), ('Transactions Director'), ('Applications Director'), ('Member');

CREATE TABLE IF NOT EXISTS Membership(Member_ID VARCHAR(9) NOT NULL UNIQUE,
						Request_ID VARCHAR(9) NOT NULL,
                        Member_Status ENUM('Active', 'Inactive', 'Probation', 'Dismissed') NOT NULL,
                        Member_Position VARCHAR(40) NOT NULL,
                        Member_DateElected DATE NOT NULL,
                        Member_Dividend DECIMAL(8, 2) DEFAULT 0.00,
                        Member_MeetAttended DATE,
                        PRIMARY KEY (Member_ID),
                        FOREIGN KEY (Request_ID) REFERENCES Request(Request_ID) ON UPDATE CASCADE,
                        FOREIGN KEY (Member_Position) REFERENCES Positions(Position));

CREATE TABLE IF NOT EXISTS CM_Payout(CMTransaction_ID INTEGER(9) NOT NULL UNIQUE,
						Member_ID VARCHAR(9) NOT NULL,
                        Transaction_Date DATE NOT NULL,
                        Transaction_Time TIME NOT NULL,
                        Amount DECIMAL(8, 2) NOT NULL,
                        PRIMARY KEY (CMTransaction_ID),
                        FOREIGN KEY (Member_ID) REFERENCES Membership(Member_ID) ON UPDATE CASCADE);
                        
                        
CREATE TABLE IF NOT EXISTS SavingsAccounts(Savings_ID VARCHAR(9) NOT NULL UNIQUE,
								Request_ID VARCHAR(9) NOT NULL,
                                Savings_Amount DECIMAL(8, 2) DEFAULT 0.00,
                                Savings_InterestRate DECIMAL(2, 2) DEFAULT 0.01,
                                Savings_PreviousInterest DECIMAL(8, 2) DEFAULT 0.00,
                                PRIMARY KEY (Savings_ID),
                                FOREIGN KEY (Request_ID) REFERENCES Request(Request_ID) ON UPDATE CASCADE);
                                
CREATE TABLE IF NOT EXISTS CS_Transaction(CSTransaction_ID INTEGER(9) NOT NULL UNIQUE,
						Savings_ID VARCHAR(9) NOT NULL,
                        Transaction_Type ENUM('Withdraw', 'Deposit') NOT NULL,
                        Transaction_Date DATE NOT NULL,
                        Transaction_Time TIME NOT NULL,
                        Amount DECIMAL(8, 2) NOT NULL,
                        PRIMARY KEY (CSTransaction_ID),
                        FOREIGN KEY (Savings_ID) REFERENCES SavingsAccounts(Savings_ID) ON UPDATE CASCADE);
                                

CREATE TABLE IF NOT EXISTS LoanRecords(Loan_ID VARCHAR(9) NOT NULL UNIQUE,
							Request_ID VARCHAR(9) NOT NULL,
                            Loan_TotalAmount DECIMAL(8, 2) NOT NULL,
                            Loan_AmountPaid DECIMAL(8, 2) DEFAULT 0.00,
                            Loan_AmountPayable DECIMAL(8, 2) NOT NULL,
                            Loan_InterestRate DECIMAL(2, 2) DEFAULT 0.03,
                            Loan_MonthlyInterest DECIMAL(8, 2) DEFAULT 0.00,
                            Loan_CumulativeInterest DECIMAL(8, 2) DEFAULT 0.00,
                            Loan_Date DATE NOT NULL,
                            Loan_Time TIME NOT NULL,
                            Loan_Status ENUM('Paying', 'Paid', 'Overdue') DEFAULT 'Paying',
                            Loan_Deadline DATE NOT NULL,
                            PRIMARY KEY (Loan_ID),
                            FOREIGN KEY (Request_ID) REFERENCES Request(Request_ID) ON UPDATE CASCADE);

CREATE TABLE IF NOT EXISTS CL_Transaction(CLTransaction_ID INTEGER(9) NOT NULL UNIQUE,
						Loan_ID VARCHAR(9) NOT NULL,
                        Transaction_Type ENUM('Loan', 'Payment') NOT NULL,
                        Transaction_Date DATE NOT NULL,
                        Transaction_Time TIME NOT NULL,
                        Amount DECIMAL(8, 2) NOT NULL,
                        PRIMARY KEY (CLTransaction_ID),
                        FOREIGN KEY (Loan_ID) REFERENCES LoanRecords(Loan_ID) ON UPDATE CASCADE);

