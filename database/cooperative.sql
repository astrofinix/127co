CREATE TRIGGER CheckAccountRequestStatus
BEFORE INSERT ON SavingsAccounts
FOR EACH ROW
BEGIN
	DECLARE State VARCHAR(40);
    
    SELECT Request_Status INTO State
    FROM Request
    WHERE Request_ID = NEW.Request_ID;
    
    IF State = 'Pending' THEN
		SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Request is being processed, awaiting for approval';
	ELSEIF State = 'Rejected' THEN
		SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Request has been denied';
	END IF;
END //

